function hasBase(w) {
  return Object.prototype.hasOwnProperty.call(BASE, w) && Array.isArray(BASE[w]);
}

function findBaseForm(word) {
  if (hasBase(word)) return word;

  for (const pfx of PREFIXES) {
    if (word.startsWith(pfx) && word.length > pfx.length + 2) {
      const rest = word.slice(pfx.length);
      if (hasBase(rest)) return rest;
    }
  }

  for (const { suffix, replace } of SUFFIXES) {
    if (word.endsWith(suffix) && word.length > suffix.length + 2) {
      const stem = word.slice(0, -suffix.length) + (replace || "");
      if (hasBase(stem)) return stem;
      if (stem.length > 2 && hasBase(stem + "e")) return stem + "e";
      if (stem.endsWith(stem[stem.length - 1]) && stem.length > 3) {
        const dedup = stem.slice(0, -1);
        if (hasBase(dedup)) return dedup;
      }
    }
  }

  for (const pfx of PREFIXES) {
    if (word.startsWith(pfx) && word.length > pfx.length + 2) {
      const rest = word.slice(pfx.length);
      for (const { suffix, replace } of SUFFIXES) {
        if (rest.endsWith(suffix) && rest.length > suffix.length + 2) {
          const stem = rest.slice(0, -suffix.length) + (replace || "");
          if (hasBase(stem)) return stem;
          if (hasBase(stem + "e")) return stem + "e";
        }
      }
    }
  }

  return null;
}

