function matchBroadPattern(word) {
  for (const [regex, root] of BROAD_PATTERNS) {
    if (root && regex.test(word)) return root;
  }
  return null;
}

function fallbackRoot(word) {
  if (word.length === 0) return "nu";
  const first = word[0].toLowerCase();
  return LETTER_DEFAULTS[first] || "nu";
}

