// MAIN: Generate the complete lookuptable
// ─────────────────────────────────────────────────────────────────────────────

function generate() {
  console.log("Loading words.json...");
  const wordsData = JSON.parse(fs.readFileSync(WORDS_PATH, "utf-8"));
  const allWords = wordsData.words;
  console.log(`Loaded ${allWords.length} words.`);

  const mappings = Object.create(null);
  let layer1 = 0, layer2 = 0, layer3 = 0, layer4 = 0;

  for (const word of allWords) {
    const lower = word.toLowerCase();

    if (Object.prototype.hasOwnProperty.call(BASE, lower) && Array.isArray(BASE[lower])) {
      mappings[lower] = { root: BASE[lower][0], true_meaning: BASE[lower][1] };
      layer1++;
      continue;
    }

    const baseForm = findBaseForm(lower);
    if (baseForm && BASE[baseForm]) {
      mappings[lower] = {
        root: BASE[baseForm][0],
        true_meaning: `from '${baseForm}': ${BASE[baseForm][1]}`,
      };
      layer2++;
      continue;
    }

    const patternRoot = matchPattern(lower);
    if (patternRoot) {
      mappings[lower] = { root: patternRoot, true_meaning: `mapped by etymological pattern` };
      layer3++;
      continue;
    }

    const broadRoot = matchBroadPattern(lower);
    if (broadRoot) {
      mappings[lower] = { root: broadRoot, true_meaning: `mapped by word-family pattern` };
      layer3++;
      continue;
    }

    const fbRoot = fallbackRoot(lower);
    mappings[lower] = { root: fbRoot, true_meaning: `mapped by letter-origin heuristic` };
    layer4++;
  }

  const output = {
    meta: {
      name: "lookuptable",
      description:
        "Maps every word in words.json to a root in root_words.json. " +
        "Each word is honored by its original meaning. " +
        "See data/METHODOLOGY.md for the full mapping approach.",
      version: "2.0.0",
      words_source: "data/words.json",
      roots_source: "data/root_words.json",
      stats: {
        total_words: allWords.length,
        total_mapped: Object.keys(mappings).length,
        layer1_base_dictionary: layer1,
        layer2_morphological: layer2,
        layer3_pattern_match: layer3,
        layer4_fallback: layer4,
      },
    },
    mappings,
  };

  console.log(`\nMapping complete:`);
  console.log(`  Layer 1 (base dictionary):       ${layer1}`);
  console.log(`  Layer 2 (morphological):          ${layer2}`);
  console.log(`  Layer 3 (pattern matching):        ${layer3}`);
  console.log(`  Layer 4 (fallback heuristic):      ${layer4}`);
  console.log(`  Total:                             ${Object.keys(mappings).length}`);

  console.log(`\nWriting ${OUTPUT_PATH}...`);
  fs.writeFileSync(OUTPUT_PATH, JSON.stringify(output, null, 2));
  console.log("Done.");
}

generate();
